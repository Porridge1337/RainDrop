public class AwesomeFirstGameMainMenuScreen implements Screen {

    private final AwesomeFirstGame game;
    private final OrthographicCamera camera;

    public AwesomeFirstGameMainMenuScreen(AwesomeFirstGame awesomeFirstGame) {
        this.game = awesomeFirstGame;
        camera = new OrthographicCamera();
        camera.setToOrtho(false, 800, 600);
    }

    @Override
    public void show() {

    }

    @Override
    public void render(float delta) {
        ScreenUtils.clear(0, 0, 0.2f, 1);
        camera.update();
        game.batch.setProjectionMatrix(camera.combined);

        game.batch.begin();
        game.font.draw(game.batch, "Welcome to Drop!!! ", 100, 150);
        game.font.draw(game.batch, "Tap anywhere to begin!", 100, 100);
        game.batch.end();
        if (Gdx.input.isKeyPressed(Input.Keys.ENTER)) {
            game.setScreen(new AwesomeFirstGameScreen(game));
            dispose();
        }
    }

    @Override
    public void resize(int width, int height) {

    }

    @Override
    public void pause() {

    }

    @Override
    public void resume() {

    }

    @Override
    public void hide() {

    }

    @Override
    public void dispose() {

    }

}
